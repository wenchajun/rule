package app

import (
	"fmt"
	"rule/pkg/exporter"
	"rule/pkg/rule"
	"rule/pkg/utils"

	"github.com/golang/glog"
	"github.com/kubesphere/event-rule-engine/visitor"
)

func eventMatch(e *rule.Auditing) {

	res := matchRule(e)
	if !res {
		return
	}

	if res && len(e.Message) > 0 {
		go exporter.Export(e)
	}

	if !e.IsAlertOnly() {
		fmt.Println(e.ToString())
	}
}

func matchRule(e *rule.Auditing) bool {

	m, err := utils.StructToMap(e.Event)
	if err != nil {
		return false
	}

	fm := utils.Flatten(m)
	rs := config.GetConfig().Rules
	archiving := false
	priority := ""
	for _, r := range rs {
		if !r.Enable || r.Type != auditing.TypeRule {
			continue
		}

		if !r.IsAlerting() && !r.IsArchiving() {
			continue
		}

		// If the event matched an archiving rule, then skip other archiving rule.
		if archiving && !r.IsAlerting() {
			continue
		}

		if archiving && !r.PriorityGreater(priority) {
			continue
		}

		c, _ := r.GetCondition(rs)
		err, ok := visitor.EventRuleEvaluate(fm, c)
		if err != nil {
			glog.Errorf("match rule[%s] error %s", r.Name, err)
			continue
		}

		if ok {
			// When the event matched multiple rules, the message is generated by the rule with the highest priority
			if r.IsAlerting() {
				e.Message = r.GetMessage(e, fm, rs)
				e.SetAlertRuleName(r.Name)
				priority = r.Priority
			}

			archiving = true
		}
	}

	return archiving
}
